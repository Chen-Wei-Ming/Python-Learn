# 變數建立
# create a var

# var is a integer
var = 1
print(var)

# var is a float
var = 1.0
print(var)

# var is a character
var = '1'
print(var)

# var is a String
var = 'string'
print(var)

# var is a Array
var = ['array1' , 'array2' , 'array3']
print(var)

# print item which in var array
for item in var :
    print(item)
